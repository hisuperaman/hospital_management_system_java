            try{
                jdbc.createConnection();
                int id = jdbc.addMedicine(nameField.getText(), sellingPriceField.getText(), buyingPriceField.getText(), quantityField.getText(), descriptionField.getText());
                populateTable();
                JOptionPane.showMessageDialog(this, "Medicine added with ID " + id + "!");
            }
            catch(Exception e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Something went wrong!");
            }
