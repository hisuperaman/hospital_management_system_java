    public MedicinesFrame() {
        initComponents();
        
        populateTable();
        
        medicineTable.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        ListSelectionModel selectionModel = medicineTable.getSelectionModel();
        selectionModel.addListSelectionListener(new ListSelectionListener(){
            @Override
            public void valueChanged(ListSelectionEvent e){
                if(!e.getValueIsAdjusting()){
                    int selectedRow = medicineTable.getSelectedRow();
                    if(selectedRow != -1){
                        String medicine_id = (String) medicineTable.getValueAt(selectedRow, 0);
                        
                        searchAndSetFields(medicine_id);
                        
                        selectionModel.clearSelection();
                    }
                }
            }
        });
    }
    
    private void populateTable(){
        try{
            DefaultTableModel tableModel = (DefaultTableModel) medicineTable.getModel();
            
            jdbc.createConnection();
            String[][] rows = jdbc.getAllMedicines();
            
            tableModel.setRowCount(0);
            for(String[] row: rows){
                tableModel.addRow(row);
            }
            
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
    
    private void searchAndSetFields(String idStr){
        System.out.println(idStr);
        if(!idStr.equals("")){
            try{
                jdbc.createConnection();
                try{
                    int id = Integer.parseInt(idStr);
                    String[] row = jdbc.getMedicine(id);
                    if(row[0]!=null){
                        idField.setText(idStr);
                        nameField.setText(row[0]);
                        sellingPriceField.setText(row[1]);                        
                        buyingPriceField.setText(row[2]);
                        quantityField.setText(row[3]);
                        descriptionField.setText(row[4]);
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "Entered ID is not present!");
                    }
                }
                catch(NumberFormatException e){
                    JOptionPane.showMessageDialog(this, "ID must be a number!");
                }
            }
            catch(Exception e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Something went wrong!");
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "ID must not be empty!");
        }
    }

